# ── primitive helpers (return plain strings, no large f-string blocks) ─────────

def svg_open(filter_id, aria_label):
    return (
        '<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 800 534"'
        ' width="800" height="534" role="img" aria-label="' + aria_label + '">\n'
        '  <defs>\n'
        '    <filter id="' + filter_id + '" x="-5%" y="-5%" width="110%" height="115%">\n'
        '      <feDropShadow dx="0" dy="2" stdDeviation="3"'
        ' flood-color="#9e3b2e" flood-opacity="0.12"/>\n'
        '    </filter>\n'
        '  </defs>\n'
        '  <rect width="800" height="534" fill="#f6f2ee"/>\n'
        '  <rect x="0" y="0" width="800" height="6" fill="#d35b2d"/>\n'
        '  <rect x="24" y="22" width="752" height="490" rx="14" fill="#e8e3dc"'
        ' stroke="#c9bfb2" stroke-width="1.5" filter="url(#' + filter_id + ')"/>'
    )

def svg_close(byline):
    return (
        '  <text x="760" y="520" font-family="Georgia, serif" font-size="8"'
        ' fill="#c9bfb2" text-anchor="end">via ' + byline + ' · Glee-fully Toolbox</text>\n'
        '</svg>'
    )

def header_bar(title, tagline, deco=""):
    lines = [
        '  <rect x="24" y="22" width="752" height="52" rx="14" fill="#d35b2d"/>',
        '  <rect x="24" y="52" width="752" height="22" fill="#d35b2d"/>',
        '  <text x="48" y="54" font-family="Georgia, serif" font-size="18"'
        ' font-weight="bold" fill="#f6f2ee" letter-spacing="0.5">' + title + '</text>',
        '  <text x="48" y="70" font-family="Georgia, serif" font-size="11"'
        ' fill="#f6f2ee" opacity="0.8" font-style="italic">' + tagline + '</text>',
    ]
    if deco:
        lines.append(deco)
    return "\n".join(lines)

def dots(x=720, y=38):
    return "\n".join([
        '  <circle cx="' + str(x) + '" cy="' + str(y) + '" r="7" fill="#f6f2ee" opacity="0.3"/>',
        '  <circle cx="' + str(x+18) + '" cy="' + str(y) + '" r="7" fill="#f6f2ee" opacity="0.5"/>',
        '  <circle cx="' + str(x+36) + '" cy="' + str(y) + '" r="7" fill="#f6f2ee" opacity="0.7"/>',
    ])

def rect(x, y, w, h, rx=0, fill="#fff", stroke="#c9bfb2", sw=1, dash="", extra=""):
    attrs = (
        ' x="' + str(x) + '" y="' + str(y) + '"'
        ' width="' + str(w) + '" height="' + str(h) + '"'
    )
    if rx:
        attrs += ' rx="' + str(rx) + '"'
    attrs += ' fill="' + fill + '"'
    if stroke and sw > 0:
        attrs += ' stroke="' + stroke + '" stroke-width="' + str(sw) + '"'
    if dash:
        attrs += ' stroke-dasharray="' + dash + '"'
    if extra:
        attrs += " " + extra
    return '  <rect' + attrs + '/>'

def txt(x, y, content, size=10, fill="#3a3530", anchor="start",
        bold=False, italic=False, opacity=None):
    attrs = (
        ' x="' + str(x) + '" y="' + str(y) + '"'
        ' font-family="Georgia, serif"'
        ' font-size="' + str(size) + '"'
        ' fill="' + fill + '"'
        ' text-anchor="' + anchor + '"'
    )
    if bold:
        attrs += ' font-weight="bold"'
    if italic:
        attrs += ' font-style="italic"'
    if opacity is not None:
        attrs += ' opacity="' + str(opacity) + '"'
    return '  <text' + attrs + '>' + content + '</text>'

def pill(x, y, w, h, fill, label, text_fill, size=9):
    cx = x + w // 2
    cy = y + h // 2 + size // 3
    return "\n".join([
        '  <rect x="' + str(x) + '" y="' + str(y) + '" width="' + str(w) +
        '" height="' + str(h) + '" rx="' + str(h//2) + '" fill="' + fill + '"/>',
        '  <text x="' + str(cx) + '" y="' + str(cy) + '" font-family="Georgia, serif"'
        ' font-size="' + str(size) + '" fill="' + text_fill + '" text-anchor="middle">' +
        label + '</text>',
    ])

def bar(x, y, w_total, w_fill, h, fill, bg="#e8e3dc", rx=3):
    return "\n".join([
        '  <rect x="' + str(x) + '" y="' + str(y) + '" width="' + str(w_total) +
        '" height="' + str(h) + '" rx="' + str(rx) + '" fill="' + bg + '"/>',
        '  <rect x="' + str(x) + '" y="' + str(y) + '" width="' + str(w_fill) +
        '" height="' + str(h) + '" rx="' + str(rx) + '" fill="' + fill + '"/>',
    ])

def circle(cx, cy, r, fill, stroke="", sw=1, opacity=None):
    attrs = ' cx="' + str(cx) + '" cy="' + str(cy) + '" r="' + str(r) + '" fill="' + fill + '"'
    if stroke:
        attrs += ' stroke="' + stroke + '" stroke-width="' + str(sw) + '"'
    if opacity is not None:
        attrs += ' opacity="' + str(opacity) + '"'
    return '  <circle' + attrs + '/>'

def line(x1, y1, x2, y2, stroke="#c9bfb2", sw=1, dash="", cap=""):
    attrs = (
        ' x1="' + str(x1) + '" y1="' + str(y1) + '"'
        ' x2="' + str(x2) + '" y2="' + str(y2) + '"'
        ' stroke="' + stroke + '" stroke-width="' + str(sw) + '"'
    )
    if dash:
        attrs += ' stroke-dasharray="' + dash + '"'
    if cap:
        attrs += ' stroke-linecap="' + cap + '"'
    return '  <line' + attrs + '/>'

def emo(x, y, ch, size=18):
    return txt(x, y, ch, size=size, anchor="middle")

def join(*args):
    return "\n".join(a for a in args if a)

